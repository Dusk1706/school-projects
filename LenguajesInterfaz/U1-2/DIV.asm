TITLE PROGRAM DIVISION CON SIGNO Y SIN SIGNO
.MODEL SMALL
.STACK 100H
.DATA
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DX, AX
    
    MOV DX, 65432
    XOR CX, CX
    MOV AX, DX
CICLO:
    XOR DX, DX
    MOV BX, 10
    DIV BX
    PUSH DX
    INC CX
    CMP AX, 0
    JNE CICLO
    
DISPLAY:
    MOV AH, 2
    POP DX
    OR DL, 30H
    INT 21H
    LOOP DISPLAY
    
    MOV AH, 4CH
    INT 21H
MAIN ENDP
    END MAIN
    